package com.example.quranonline.data.model;

public class Azkar {
    public int id;
    public  String Name;
    public String reader_name;
    public String reader_img;
    public String link;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        this.Name = name;
    }

    public String getReader_name() {
        return reader_name;
    }

    public void setReader_name(String reader_name) {
        this.reader_name = reader_name;
    }

    public String getReader_img() {
        return reader_img;
    }

    public void setReader_img(String reader_img) {
        this.reader_img = reader_img;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
